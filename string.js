// string methods

// charAt() 
var Name="shivay";
console.log(Name.length); //to find the length of string.
console.log("----print the character position index");
Name.charAt(3);
console.log(Name);

// concat()
console.log("----cancating strings");
console.log("shi".concat("va").concat("y!"));
 

// indexOf()
console.log("----Displays the index of given string!")
console.log("shivay!".indexOf("i"));
console.log("shivay!".indexOf("v"));

// lastIndexOf()
console.log("----Displays the last index or last match of index")
console.log("shivaji".lastIndexOf('i'));
 
// split() 
console.log("----split the given string")
console.log("hello, world".split(","));

// toUpperCase() 
console.log("----Display the upper case of given string")
console.log(Name.toUpperCase());

// toLowerCase()
console.log("----display the lower case of given string")
console.log(Name.toLowerCase());

